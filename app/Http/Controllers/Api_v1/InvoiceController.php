    {
        $invoices = Invoice::where([
            ['work_id', '=', $request->query('work_id')],
            ['provider_id', '=', $request->query('provider_id')]
        ])->orderBy('signed_at')->get();
        return InvoiceResources::collection($invoices);
    }

